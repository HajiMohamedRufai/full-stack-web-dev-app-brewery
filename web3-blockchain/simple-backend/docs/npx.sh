npx is a tool that comes with node.js.
it stands for node package execute
It is used to run node.js packagesg that are not installed globally in your system
eg the create-react-app package

How is it installed?
npm i npx

Instead of installing it globally which probably is gonna take more space
npm install -g create-react-app

You can directly create a new react app with the npx:
npx create-react-app my-app && cd my-app && npm start  # to start the react app
